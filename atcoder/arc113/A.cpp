void solve_me()
{
  int n, c=0;
  cin>>n;
  for(int i=1;i<=n;i++)
  {
    for(int j=1;j<=n;j++)
    {
      if(i*j > n)break;
      for(int k=1;k<=n;k++)
      {
        if( i*j*k <= n)c++;
        else break;
      }
    }
  }
  cout<<c;
  return;
}
